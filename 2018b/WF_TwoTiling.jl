function getidx(l1::String,l2::String,l3::String)
    a::UInt64 = 0
    x::String = l1*l2*l3
    for i in 1:9; if x[i] == '@'; a |= (1 << (i-1)); end; end
    a = reduceTile(a)
    c = genCopies(a)
    return minimum(c)
end

