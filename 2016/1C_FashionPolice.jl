
using Random
infile = stdin
## Type Shortcuts (to save my wrists and fingers :))
const I = Int64; const VI = Vector{I}; const SI = Set{I}; const PI = NTuple{2,I};
const TI = NTuple{3,I}; const QI = NTuple{4,I}; const VPI = Vector{PI}; const SPI = Set{PI}
const VC = Vector{Char}; const VS = Vector{String}; VB = Vector{Bool}; VVI = Vector{Vector{Int64}}
const F = Float64; const VF = Vector{F}; const PF = NTuple{2,F}

gs()::String = rstrip(readline(infile))
gi()::Int64 = parse(Int64, gs())
gf()::Float64 = parse(Float64,gs())
gss()::Vector{String} = split(gs())
gis()::Vector{Int64} = [parse(Int64,x) for x in gss()]
gfs()::Vector{Float64} = [parse(Float64,x) for x in gss()]

function solve(J::I,P::I,S::I,K::I)::Tuple{I,VS}
    if S <= K; return (J*P*S, ["$i $j $k" for i in 1:J for j in 1:P for k in 1:S]); end
    return (J*P*K, ["$i $j $(1+(i+j+k) % S)" for i in 1:J for j in 1:P for k in 1:K])
end

function main(infn="")
    global infile
    infile = (infn != "") ? open(infn,"r") : length(ARGS) > 0 ? open(ARGS[1],"r") : stdin
    tt::I = gi()
    for qq in 1:tt
        print("Case #$qq: ")
        J,P,S,K = gis()
        ans = solve(J,P,S,K)
        print("$(ans[1])\n")
        for ss in ans[2]; print("$ss\n"); end
    end
end

Random.seed!(8675309)
main()

