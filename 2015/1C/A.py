def printOutput(tt,ans) :
    print("Case #%d: %s" % (tt,ans))

def getInputs(IN) :
    r,c,w = IN.ints()
    return (r,c,w)

def check(b,n,m,test) :
    tally = sum( 1 + test//x for x in m )
    return tally >= n

def solve(inp) :
    (r,c,w) = inp

    ## It take c//w to rule out each row
    ans = (r-1) * (c // w)

    ## On the last row, if c is a multiple of w, then we only need (w-1) turns after the first hit
    ## Otherwise, we need w turns, as we will necessarily have one more miss
    if c % w == 0 : ans += (c // w) + (w-1)
    else          : ans += (c // w) + w
    return "%d" % ans

